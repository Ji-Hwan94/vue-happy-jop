<template>
    <div>
        <p class="Location">
            <span class="btn_nav bold">시설 관리</span>
            <span class="btn_nav bold"> 강의실</span>
        </p>
        <p class="conTitle">
            <span class="conNm">강의실</span>
            <span class="fr">
                <span>강의실 명 </span>
                <input type="text" class="lecure-name" v-model="searchRoomName" />
                <button class="btn btn-light btn-sm" @click="$router.push('write')">강의실 신규등록</button>
                <button class="btn btn-secondary btn-sm" @click="searchLecture()">검색</button>
            </span>
        </p>
        <div>
            <b> 총건수 : {{ total }} 현재 페이지 번호 : {{ currentPage }} </b>
        </div>
        <div class="row">
            <CardLecture v-for="(data, i) in dataList" :key="i" :data="data"></CardLecture>
        </div>
        <Pagination v-bind="{ currentPage, totalItems: total, itemsPerPage: 6 }" @search="searchLecture($event)" />
    </div>
</template>

<script setup>
import CardLecture from './CardLecture.vue';
import { onMounted, ref } from 'vue';
import Pagination from '@/components/common/PaginationComponent.vue';
import { axiosAction } from '.';
import { SamplePage5 } from '@/api/api';

const dataList = ref([]);
const total = ref(0);
const currentPage = ref(0);
const searchRoomName = ref('');

const searchLecture = async (cpage) => {
    cpage = cpage || 1;

    let params = new URLSearchParams();
    params.append('cpage', cpage);
    params.append('pagesize', 6);
    params.append('searchRoomName', searchRoomName.value);

    const lectureList = await axiosAction(SamplePage5.LectureRoomList, params);
    if (lectureList) {
        dataList.value = lectureList.listdata;
        total.value = lectureList.listcnt;
        currentPage.value = cpage;
    }

    // axios
    //     .post('/adm/lectureRoomListjson.do', params)
    //     .then((res) => {
    //         dataList.value = res.data.listdata;
    //         total.value = res.data.listcnt;
    //         currentPage.value = cpage;
    //     })
    //     .catch((err) => {
    //         alert(err.message);
    //     });
};
onMounted(() => {
    searchLecture();
});
</script>

